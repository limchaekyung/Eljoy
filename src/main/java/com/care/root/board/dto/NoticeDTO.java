package com.care.root.board.dto;

public class NoticeDTO {
	private int notice_no;
	private String notice_group;
	private String notice_content;
	private String notice_writedate;
	private String notice_title;
	private int notice_hit;
	public int getNotice_no() {
		return notice_no;
	}
	public void setNotice_no(int notice_no) {
		this.notice_no = notice_no;
	}
	public String getNotice_group() {
		return notice_group;
	}
	public void setNotice_group(String notice_group) {
		this.notice_group = notice_group;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_writedate() {
		return notice_writedate;
	}
	public void setNotice_writedate(String notice_writedate) {
		this.notice_writedate = notice_writedate;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public int getNotice_hit() {
		return notice_hit;
	}
	public void setNotice_hit(int notice_hit) {
		this.notice_hit = notice_hit;
	}
	
	
}
